package com.snazzyatoms.proshield.plots;

/**
 * Represents a simple claim (single point for now, expandable to areas later)
 */
public class Claim {
    private final String world;
    private final int x;
    private final int z;

    public Claim(String world, int x, int z) {
        this.world = world;
        this.x = x;
        this.z = z;
    }

    public String getWorld() {
        return world;
    }

    public int getX() {
        return x;
    }

    public int getZ() {
        return z;
    }
}
